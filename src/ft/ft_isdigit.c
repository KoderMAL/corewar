#include "ft.h"

int			ft_isdigit(int c)
{
	return (c >= '0' && c <= '9');
}

int			ft_str_isdigit(char *s)
{
	int		i;

	i = 0;
	if (s[0] == '-' || s[0] == '+')
		i++;
	while (s[i])
	{
		if (ft_isdigit(s[i]) == 0)
			return (0);
		i++;
	}
	return (1);
}
