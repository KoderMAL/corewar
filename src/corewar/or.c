#include "vm.h"

void			op_or(t_thread *pc)
{
	int		param[2];
	int		i;

	i = 0;
	while (i < 2)
	{
		param[i] = get(pc, i);
		i++;
	}
	pc->r[pc->params[2]] = param[0] | param[1];
	return ;
}
